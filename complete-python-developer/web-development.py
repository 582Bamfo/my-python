# Flask
# Flask is a web framework that provides the tools and libraries needed to build web applications in Python. It includes a built-in development server, but this server is not suitable for production use. The development server is useful for testing and development because it automatically reloads the application when changes are made to the code.

# Flask is a lightweight and flexible web framework. It is often described as a "micro-framework" 


