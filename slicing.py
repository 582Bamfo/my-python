#fetching data or charaters from data type

#slicing a str
planet1 = "closest to sun"
print(planet1[0:])
print(planet1[8])
print(planet1[0:3])
print(planet1[7:11])
print(planet1[11:14])

#slicing a tuple
devops = ("linux","vagrant", "bash script","aws","jenkins","python")
print(devops[0])
print(devops[1:3])
print(devops[-1])
print(devops[1:3][0])
print(devops[1:3][0] [2:7])
print(devops[1:3][0][2:7] [2])